package com.emrs.entity;

/**
 * @author Administrator
 *
 */
public class Customer {
	private int userId;
	private String userName;
	private String name;
	private String password;
	private String userClass;
	private String majorCourse;
	private String idCard;
	private String telNember;
	/**
	 * @return the userId
	 */
	public int getUserId() {
		return userId;
	}
	/**
	 * @param userId the userId to set
	 */
	public void setUserId(int userId) {
		this.userId = userId;
	}
	/**
	 * @return the userName
	 */
	public String getUserName() {
		return userName;
	}
	/**
	 * @param userName the userName to set
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}
	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}
	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}
	
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the userClass
	 */
	public String getUserClass() {
		return userClass;
	}
	/**
	 * @param userClass the userClass to set
	 */
	public void setUserClass(String userClass) {
		this.userClass = userClass;
	}
	/**
	 * @return the majorCourse
	 */
	public String getMajorCourse() {
		return majorCourse;
	}
	/**
	 * @param majorCourse the majorCourse to set
	 */
	public void setMajorCourse(String majorCourse) {
		this.majorCourse = majorCourse;
	}
	/**
	 * @return the idCard
	 */
	public String getIdCard() {
		return idCard;
	}
	/**
	 * @param idCard the idCard to set
	 */
	public void setIdCard(String idCard) {
		this.idCard = idCard;
	}
	/**
	 * @return the telNember
	 */
	public String getTelNember() {
		return telNember;
	}
	/**
	 * @param telNember the telNember to set
	 */
	public void setTelNember(String telNember) {
		this.telNember = telNember;
	}
	
}
